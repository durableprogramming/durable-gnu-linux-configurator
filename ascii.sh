# Select a random ASCII art from the array
random_index=$((RANDOM % ${#ascii_art[@]}))
selected_art=${ascii_art[$random_index]}

colors=()
# Base HSL values
base_hue=$((RANDOM % 360))  # Random hue (0-359)
saturations=(100 90 80 85 95 100 90)
luminances=(65 55 50 60 45 70 60)

for i in {0..6}; do
  # Convert HSL to terminal color code
  # Using terminal color approximation since bash can't do HSL->RGB conversion directly
  # We're using 6x6x6 color cube (216 colors) from 16-231 in xterm-256
  
  # Adjust hue for variation while maintaining similar palette
  current_hue=$(( (base_hue + i*5) % 360 ))
  saturation=${saturations[$i]}
  luminance=${luminances[$i]}
  
  # Map HSL to xterm-256 color space (very approximate)
  # Using simplified formula to map to the color cube
  r=$(( 5 * (current_hue < 60 || current_hue >= 300 ? 1 : 0) + (luminance / 20) ))
  g=$(( 5 * (current_hue >= 60 && current_hue < 180 ? 1 : 0) + (luminance / 20) ))
  b=$(( 5 * (current_hue >= 180 && current_hue < 300 ? 1 : 0) + (luminance / 20) ))
  
  # Ensure values are in range 0-5
  r=$(( r > 5 ? 5 : r ))
  g=$(( g > 5 ? 5 : g ))
  b=$(( b > 5 ? 5 : b ))
  
  # Calculate the color code (16 + 36*r + 6*g + b)
  color_code=$(( 16 + 36*r + 6*g + b ))
  
  colors+=("\033[38;5;${color_code}m")
done

# Split the ASCII art into lines
IFS=$'\n' read -rd '' -a lines <<<"$selected_art"

# Print each line with the corresponding color
for i in "${!lines[@]}"; do
	color_index=$((i % ${#colors[@]}))
	echo -e "${colors[color_index]}${lines[i]}"
done

echo 
echo -e "\t\t" '"Yeah, that is us." -- Someone, probably.'

